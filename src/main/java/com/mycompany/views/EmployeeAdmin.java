    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String valor = (String) tblTable.getValueAt(tblTable.getSelectedRow(), 2);
        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        if (tblTable.getSelectedRows().length < 1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar uno o mÃ¡s usuarios a eliminar.\n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            if ("Veterinario".equals(valor)) {
                for (int i : tblTable.getSelectedRows()) {
                    try {
                        DAOVeterinario dao = new DAOVeterinarioImplements();
                        String nombre = (String) tblTable.getValueAt(tblTable.getSelectedRow(), 0);
                       
                        Veterinario vet = new Veterinario(nombre, "", "");
                        dao.eliminar(vet.getNombre_usuario());
                        model.removeRow(i);
                    } catch (Exception e) {
                        System.out.println(e.getMessage());
                    }
                }

            } else if(valor.toLowerCase().equals("admin") || valor.toLowerCase().equals("empleado")){
                for (int i : tblTable.getSelectedRows()) {
                    try {
                        DAOEmpleado dao = new DAOEmpleadoImplements();
                        String nombre = (String) tblTable.getValueAt(tblTable.getSelectedRow(), 0);
                        
                        Empleado emp = new Empleado(nombre, "", valor, "");
                        dao.eliminar(emp.getNombre_usuario());
                        model.removeRow(i);
                    } catch (Exception e) {
                        System.out.println(e.getMessage());
                    }
                }
            }

        }
        

    }//GEN-LAST:event_btnDeleteActionPerformed

